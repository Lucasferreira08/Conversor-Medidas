#include "conversor_massa.h"

// Implementação das funções
float gramas_para_quilogramas(float gramas) {
    return gramas / 1000.0;
}

float quilogramas_para_gramas(float quilogramas) {
    return quilogramas * 1000.0;
}

float quilogramas_para_toneladas(float quilogramas) {
    return quilogramas / 1000.0;
}

float toneladas_para_quilogramas(float toneladas) {
    return toneladas * 1000.0;
}
